#Escape Sequences.................
#newline
name = "i am parikshan shahi\n i am from jiwakhola.."
print(name)
#backslash
text = "here we go\\ we are back.."
print(text)
#tab
txt = "hello\tworld"
print(txt)
#double quote
code = 'parikshan\" i am from nepal'
print(code)
#single quote
var = 'my brother\'s name is parikshan shahi '
print(var)